        <p class="title1" style="margin-top:30px;">手工纸公仔的制作工具及技巧</p>
              <p class="">1. 印表机 <br>
                彩色印表机如Epson或HP都是不错的机种，大部分纸模型的页面都是A4，所以A4尺寸机型即可。</p>
              <p>2. 纸张 <br>
                太硬的纸不好弯曲；而太软的纸也不够坚固。彩色喷墨印表纸是最佳选择，厚度问题可以在列印完后，用完稿用喷胶表一层较厚的的纸上，如此不但列印品质可以掌握，防止遇胶颜色煳掉，又可增加厚度。Epson的印表纸是上上之选，小蜜蜂印表纸是经济型，越好的纸就列印越清晰，而且防止油墨煳掉</p>
              <p>3. 切割刀 （家长使用）<br>
                切割工具最多使用的是笔刀或美工刀。笔刀手感舒适,前端有防滑设计,刀片坚硬并可以更换，整体优于美工刀。刀面要锐利，才不至割破纸边。刀背可以当作画线的工具，划线后折叠会比较顺利</p>
              <p>4. 剪刀（儿童使用） <br>
                市场上有专为儿童设计的安全剪刀。可以让孩子剪切较大零件，在处理比较小的部分的时候，父母可以用切割刀帮助孩子完成。</p>
              <p>5. 钢尺 <br>
                要切割直线时必备，塑胶尺子边缘很容易被刀锋划损和打滑。另外请注意别让尺的印刷面把纸弄髒了</p>
              <p>6. 钳子 <br>
                当粘贴零件时,会出现狭小空间,可以用镊子探入夹牢固定。一般文具店销售的有尖嘴 鹰嘴 平嘴，挑选时要注意整体硬度和头部的咬合度.</p>
              <p>7. 圆杆铅笔 <br>
                可用来协助弯曲纸张，或是让黏贴部黏的更牢（用杆的）</p>
              <p>8. 牙籤 <br>
                可用来涂抹黏胶，也可用来支撑较脆弱的部分</p>
              <p>9. 切割垫<br>
                直接在桌面切割零件会留下划痕，使用切割垫不但保护了桌面，也可以保护刀尖,延长使用寿命。切割垫有自动愈合功能,常见为A3、A4型号。用平坦的厚纸板也可代替。</p>
              <p>10. 白胶 <br>
                普通胶水含水份较多,会使纸张变形,所以纸模型需要使用含水少的胶水来粘贴。白乳胶不会像速干胶一样立即粘牢,会留有调整的时间,并且很容易购买到，市场上很多不同品牌的白胶，质量参差，如果觉得粘度不够，可以打开盖子挥发一下再行使用。注意，选用的胶在乾掉后要为透明的！</p>
